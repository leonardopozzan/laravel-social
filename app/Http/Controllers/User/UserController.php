    public function store(Request $request)
    {
        $form_data = $request->all();

        //dd($form_data);
        // $new_user = User::create($form_data);
        $new_user = new User;
        $new_user->fill($form_data);
        $new_user->password = bcrypt($new_user->password);
        $new_user->subscribed_at = today()->toDateString();
        $new_user->save();
        return view('loginform')->with(array('registered' => 1));
    }

